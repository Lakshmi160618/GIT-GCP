def greet_with_message(name, message="Hello"):
    print(f"{message}, {name}!")

# Call the function
greet_with_message("Alice")
greet_with_message("Bob", "Hi")

"""
In this example, the greet_with_message function takes two parameters, name and message, with message having a default value of "Hello." You can call the function with just a name or provide a custom message.
"""