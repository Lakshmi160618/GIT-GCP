def greet_person(name, greeting="Hello"):
    print(f"{greeting}, {name}!")

# Call the function with keyword arguments
greet_person(name="Alice")
greet_person(name="Bob", greeting="Hi")


'''
In this example, the greet_person function accepts two parameters, name and greeting, with greeting having a default value of "Hello." You can call the function using keyword arguments to specify the values for the parameters.
'''